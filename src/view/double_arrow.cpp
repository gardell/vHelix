void arrow_image_call_teximage3d() {
	/*
	 * Notice that we only pick up the alpha channel by unpacking with a stride of 3 thus using the R-channel only
	 */
	GLCALL(glPushClientAttrib(GL_CLIENT_PIXEL_STORE_BIT));
	GLCALL(glPixelStorei(GL_UNPACK_ALIGNMENT, 1));

	GLCALL(glTexImage3D(GL_TEXTURE_3D, 0, GL_RGB8, arrow_image.width, arrow_image.height / 2, 2, 0, GL_RGB, GL_UNSIGNED_BYTE, arrow_image.pixel_data));

	GLCALL(glPopClientAttrib());
}